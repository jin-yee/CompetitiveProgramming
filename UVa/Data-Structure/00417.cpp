#include <bits/stdc++.h>

using namespace std;


int main() {

	map<string, long long> mm;
	queue<string> alphabet;
	string s;
	int counter = 1;

	for(char ch = 'a'; ch <= 'z'; ch++){
		s = ch;
		alphabet.push(s);
	}

	for(int i = 1; i <= 83700; i ++){ // how many combination from a, b, ..., vwxyz
		s = alphabet.front();
		for(char ch = s[s.size() - 1] + 1; ch <= 'z'; ch++){ // + 1 to skip string such as aa, bb as they're not allowed
			string temp = s;
			temp += ch;
			alphabet.push(temp);
		}
		mm[s] = counter++;
		alphabet.pop();
	}

	while(cin >> s) cout << mm[s] << endl;

	return 0;
}